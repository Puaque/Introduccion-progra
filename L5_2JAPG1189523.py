print("Ejercicio 2")

X=input("Ingrese un número de 1 a 7: ")
xnumero=int(X)

if xnumero==1:
    print("El día es Lunes")
elif xnumero==2:
    print("El día es Martes")
elif xnumero==3:
    print("El día es Miercoles")
elif xnumero==4:
    print("El día es Jueves")
elif xnumero==5:
    print("El día es Viernes")
elif xnumero==6:
    print("El día es Sábado")
elif xnumero==7:
    print("El día es Domingo")
else:
    print("ERROR, el número ingresado debe ser de uno a siete")